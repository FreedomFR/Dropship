    {
        parent::__construct($registry, Page::class);
    }

    public function findAllVisibleArticles()
    {
        return $this->createQueryBuilder('p')
        ->where('p.isBlog = 1')
        ->andWhere('p.private = 0')
        ->orderBy('p.id', 'DESC')
        ->getQuery()
        ->getResult();
    }

